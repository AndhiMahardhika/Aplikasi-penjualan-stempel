    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        if(pilihkd.getSelectedItem().equals("")|| tjml.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "MAHAR STAMP", JOptionPane.INFORMATION_MESSAGE);
        
        }else{
            String pilihkdd = (String)pilihkd.getSelectedItem();
            String jml = tjml.getText();
           
            try {
                Connection c = koneksi.getKoneksi();
                
                String sql = "INSERT INTO list_brng VALUES (?, ?, ?)";
                
               
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, null);
                p.setString(2, pilihkdd);
                p.setString(3, jml);
                
                p.executeUpdate();
                p.close();
            } catch (SQLException e) {
                System.out.println("Terjadi Error");
            } finally {
                pilihkd.setSelectedItem("");
                tjml.setText("");
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "MAHAR STAMP", JOptionPane.INFORMATION_MESSAGE);
                loadData();
                
                
            }
        }
    }//GEN-LAST:event_btntambahActionPerformed

    private void btnselesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnselesaiActionPerformed
        // TODO add your handling code here:
        if(pilihkdsup.getSelectedItem().equals("") ||tnama.getText().equals("")||ttlp.getText().equals("")||  pilihkaryawan.getSelectedItem().equals("")|| tadmin.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "MAHAR STAMP", JOptionPane.INFORMATION_MESSAGE);
        
        }else{
            String pilihksup = (String)pilihkdsup.getSelectedItem();
            String namaa = tnama.getText();
            String tlp = ttlp.getText();
            String pilihkp = (String)pilihkaryawan.getSelectedItem();
            String admin = tadmin.getText();
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT * FROM list_brng";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                long millis=System.currentTimeMillis();  
                java.sql.Date date=new java.sql.Date(millis);  
                System.out.println(date); 
                String tgl = date.toString();
                String sqla = "INSERT INTO restock VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
         
                PreparedStatement p = c.prepareStatement(sqla);
                p.setString(1, r.getString("kd_barang"));
                p.setString(2, r.getString("jumlah"));
                p.setString(3, pilihksup);
                p.setString(4, namaa);
                p.setString(5, tlp);
                p.setString(6, pilihkp);
                p.setString(7, admin);
                p.setString(8, tgl);
                
                p.executeUpdate();
                p.close();
                
                
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }finally{
         try {
            String sqla ="TRUNCATE `list_brng";
            java.sql.Connection conn=(Connection)koneksi.getKoneksi();
            java.sql.PreparedStatement pst=conn.prepareStatement(sqla);
            pst.execute();
            JOptionPane.showMessageDialog(null, "TRANSAKSI SELESAI", "Mahar Stamp", JOptionPane.INFORMATION_MESSAGE);
            loadData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        }
        }
    }//GEN-LAST:event_btnselesaiActionPerformed

