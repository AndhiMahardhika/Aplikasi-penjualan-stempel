    private void pilihkdsupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pilihkdsupActionPerformed
        // TODO add your handling code here:
        if (pilihkdsup.getSelectedItem().equals("pilih Kode Suplier")){
            tnama.setText("");
            ttlp.setText("");
        }else{
            try {
                Connection c = koneksi.getKoneksi();
                Statement s = c.createStatement();

                String sql = "SELECT nama_suplier,no_tlp FROM suplier WHERE kd_suplier ='" + pilihkdsup.getSelectedItem() + "'";
                ResultSet r = s.executeQuery(sql);

                while (r.next()) {
                    tnama.setText(r.getString("nama_suplier"));
                    ttlp.setText(r.getString("no_tlp"));
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_pilihkdsupActionPerformed

