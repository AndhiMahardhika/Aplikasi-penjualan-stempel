    private void btsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsimpanActionPerformed
        // TODO add your handling code here:
        if(tresi.getText().equals("") || cbidkaryawan.getSelectedItem().equals("")|| tnamakaryawan.getText().equals("")||cbidkonsumen.getSelectedItem().equals("")|| tnamakonsumen.getText().equals("")||talamat.getText().equals("")||cbkodepesan.getSelectedItem().equals("")|| totalharga.getText().equals("")||tongkir.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "MAHAR STAMP", JOptionPane.INFORMATION_MESSAGE);
            }else{
            String resi = tresi.getText();
            String idkaryawan = (String)cbidkaryawan.getSelectedItem();
            String idkonsumen = (String)cbidkonsumen.getSelectedItem();
            String namakaryawan = tnamakaryawan.getText();
            String namakonsumen = tnamakonsumen.getText();
            String alamat = talamat.getText();
            String kodepesan = (String)cbkodepesan.getSelectedItem();
            String total = totalharga.getText();
            String ongkir = tongkir.getText();
            
            
            try {
                Connection c = koneksi.getKoneksi();
                
                String sql = "INSERT INTO pengiriman VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, null);
                p.setString(2, resi);
                p.setString(3, idkaryawan);
                p.setString(4, namakaryawan);
                p.setString(5, idkonsumen);
                p.setString(6, namakonsumen);
                p.setString(7, alamat);
                p.setString(8, kodepesan);
                p.setString(9, total);
                p.setString(10, ongkir);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Data berhasil Disimpan", "Mahar Stamp", JOptionPane.INFORMATION_MESSAGE);
            }catch (SQLException e) {
                System.out.println("Terjadi Error");
            } finally {
                nokd();
            tnamakaryawan.setText("");
            tnamakonsumen.setText("");
            talamat.setText("");
            totalharga.setText("");
            tongkir.setText("");
        }
        }
    }//GEN-LAST:event_btsimpanActionPerformed

