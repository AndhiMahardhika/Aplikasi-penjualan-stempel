    private void nokd() {
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();

            String sql = "SELECT * FROM pengiriman ORDER by noresi desc";
            ResultSet r = s.executeQuery(sql);

            if (r.next()) {
                String nokd = r.getString("noresi").substring(1);
                String AN = "" + (Integer.parseInt(nokd) + 1);
                String Nol = "";

                if (AN.length() == 1) {
                    Nol = "000";
                } else if (AN.length() == 2) {
                    Nol = "00";
                } else if (AN.length() == 3) {
                    Nol = "0";
                } else if (AN.length() == 4) {
                    Nol = "";
                }

                tresi.setText("R" + Nol + AN);
            } else {
                tresi.setText("R0001");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
